export function searchSchools(query: string, limit = 12): School[] {
  const q = query.toLowerCase().trim()
  if (q.length < 2) return []
  return SCHOOLS
    .filter(s =>
      s.name.toLowerCase().includes(q) ||
      s.city.toLowerCase().includes(q) ||
      s.state.toLowerCase() === q
    )
    .slice(0, limit)
}
